package TestesDeConhecimento;

public class Indice 
{
	public static void main(String[] args) 
	{
		//Aqui criei as variaveis
		int INDICE = 13;
		int SOMA = 0;
		int K = 0;

		while (K < INDICE) 
		{
		    K = K + 1;
		    SOMA = SOMA + K;
		}

		System.out.println(SOMA);
		
		//valor igual 91

	}
	
}
